    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
       int linhaSelecionada = -1;
        linhaSelecionada = tabela.getSelectedRow();
        if (linhaSelecionada >= 0) {
            int idPlano = (int) tabela.getValueAt(linhaSelecionada, 0);
            PlanoAcessoController planoC = new PlanoAcessoController();
            if (planoC.remove(idPlano)) {
                modelo.removeRow(linhaSelecionada);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Nenhuma linha foi selecionada");
        }
    }//GEN-LAST:event_btExcluirActionPerformed

    private void txPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txPesquisarActionPerformed
        String nome = txPesquisar.getText();
       PlanoAcessoController planoC = new PlanoAcessoController();
       modelo.setNumRows(0);
      for(PlanoAcesso plano: planoC.listByNome(nome)){
          modelo.addRow(new Object[]{plano.getId(), plano.getNome(), plano.getDescricao(), plano.getVelocidade(), plano.getDownload(), plano.getUpload()});
      }
    }//GEN-LAST:event_txPesquisarActionPerformed

    
