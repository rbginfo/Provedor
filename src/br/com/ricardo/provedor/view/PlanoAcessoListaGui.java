    private void criaJTable() {
        tabela = new JTable(modelo);
        modelo.addColumn("Código");
        modelo.addColumn("Nome");
        modelo.addColumn("Descrição");
        modelo.addColumn("Velocidade");
        modelo.addColumn("Download");
        modelo.addColumn("Upload");
        preencherJTable();
    }
    
    private void preencherJTable(){
        PlanoAcessoController planoC = new  PlanoAcessoController();
        for(PlanoAcesso plano :planoC.listarTodos()){
            modelo.addRow(new Object[]
            {plano.getId(), plano.getNome(), plano.getDescricao(), plano.getVelocidade(), plano.getDownload(), plano.getUpload()});
        }
    }
