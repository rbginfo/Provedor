    private void validarLogin() {
        FuncionarioController fc = new FuncionarioController();
        JanelaPrincipal jp = new JanelaPrincipal();

        String login = txLogin.getText();
        String senha = new String(txSenha.getPassword());

        boolean resposta = fc.validaLogin(login, senha);

        if (resposta == true) {
            jp.setVisible(true);
            jp.setExtendedState(jp.MAXIMIZED_BOTH);
            System.out.println("Login realizado com sucesso!");
            this.dispose();

        } else if (txLogin.getText().equals("") && new String(txSenha.getPassword()).equals("")) {
            JOptionPane.showMessageDialog(null, "Os campos n√£o foram preenchidos!");
        } else {
            JOptionPane.showMessageDialog(null, "Usuario ou Senha incorretos!");
            txLogin.setText("");
            txSenha.setText("");
        }
    }
    
